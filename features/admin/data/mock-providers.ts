export function getProviderRequestDetail(id: string): ProviderRequestDetail | null {
  if (MOCK_PROVIDER_REQUEST_DETAILS[id]) {
    return MOCK_PROVIDER_REQUEST_DETAILS[id];
  }

  const request = MOCK_PROVIDER_REQUESTS.find((item) => item.id === id);

  if (!request) {
    return null;
  }

  const [firstName, ...lastNameParts] = request.name.split(" ");

  return {
    id: request.id,
    firstName: firstName ?? request.name,
    lastName: lastNameParts.join(" ") || "—",
    email: request.email,
    phone: request.phone,
    npiNumber: "32742376547623",
    credentials: request.education.split(",")[0] ?? "—",
    licenseNumber: "gts23482",
    licenseExpirationDate: "12/23/2029",
    licenseState: "AL",
    homeStreetAddress: "72 Caisson Trace,",
    homeCity: "Spanish Fort",
    homeState: "AL",
    homeZipCode: "3724627",
    practiceAddress: "72 Caisson Trace, Spanish Fort, AL, 1236745",
    status: request.status,
  };
}
